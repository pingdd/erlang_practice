-module(mylists).
-author("duping").

%% API
-export([sum/1, map/2]).


sum([H | T]) -> H + sum(T);
sum([]) -> 0.


map(F, [H | T]) -> [F(H) | map(F, T)];
%%使用 _ 匹配任意不关注的对象
map(_, []) -> [].